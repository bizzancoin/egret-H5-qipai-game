
package com.idealighter.game.dictionary.domain;


public class SysconfigDomain {

  // id
  private int id;
  // 名称
  private String val;
  // 值
  private String desc;

  public int getId() {
    return id;
  }

  public void setId(int id) {
    this.id = id;
  }

  public String getVal() {
    return val;
  }

  public void setVal(String val) {
    this.val = val;
  }

  public String getDesc() {
    return desc;
  }

  public void setDesc(String desc) {
    this.desc = desc;
  }

}
